export function getHintsByCategory(category: string): APIHint[] {
  return zdjlAPIHints.filter(hint => hint.category === category)
}

export function searchHints(query: string): APIHint[] {
  const lowerQuery = query.toLowerCase()
  return zdjlAPIHints.filter(hint => 
    hint.name.toLowerCase().includes(lowerQuery) ||
    hint.description.toLowerCase().includes(lowerQuery) ||
    hint.category.toLowerCase().includes(lowerQuery)
  )
}
